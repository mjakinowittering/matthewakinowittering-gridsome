<template>
  <Layout>
    <div class="container">
      <div class="columns is-multiline">
        <div class="column is-one-third-desktop is-flex" v-for="post in $page.posts.edges" :key="post.node.id">
          <Card :post="post" />
        </div>
      </div>
      <Pager :info="$page.posts.pageInfo"/>
    </div>
  </Layout>
</template>


<page-query>
  query($page: Int) {
    posts: allPost(filter: {}, sortBy: "publishedAt", order: DESC, perPage: 9, page: $page) @paginate {
      pageInfo {
        totalPages
        currentPage
      }
      edges {
        node {
          path
          title
          subtype
          authors
          figure {
            img
          }
          youtube {
            src
          }
          publishedAt(format: "YYYY-MM-DD")
          excerpt
        }
      }
    }
  }
</page-query>


<script>
  import { Pager } from 'gridsome'
  import Card from '~/components/Blog/Card.vue'
  export default {
    metaInfo() {
      return {
        title: 'Blog',
      }
    },
    components: {
      Pager,
      Card
    },
    methods: {
      breadcrumbFactory() {
        if (this.$page.posts.pageInfo.currentPage > 1) {
          return [
            {
              to: '/',
              label: 'Home'
            },
            {
              to: '/blog/',
              label: 'Blog'
            },
            {
              to: `/blog/${this.$page.posts.pageInfo.currentPage}/`,
              label: `Page ${this.$page.posts.pageInfo.currentPage}`
            }
          ]
        } else {
          return [
            {
              to: '/',
              label: 'Home'
            },
            {
              to: '/blog/',
              label: 'Blog'
            }
          ]
        }
      }
    }
  }
</script>
